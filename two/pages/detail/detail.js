  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    var id = options.id;
    var detailObj = null;
    for (var i in proDetail.data){
      for (var j in proDetail.data[i]){
        var proId = proDetail.data[i][j].id;
        if (proId==id){
          detailObj = proDetail.data[i][j];
          this.setData({
            pro: detailObj
          })
        }
      }
    } 
  },

